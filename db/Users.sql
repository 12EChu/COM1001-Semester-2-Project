CREATE TABLE users (
    id INTEGER PRIMARY KEY,
    first_name TEXT,
    surname TEXT,
    email TEXT,
    password TEXT,
    privilege TEXT,
    title TEXT,
    job_Title TEXT,
    industry_Sector TEXT,
    available_Time TEXT,
    university TEXT,
    degree TEXT,
    telephone TEXT,
    description INTEGER,
    has_mentee INTEGER,
    has_mentor INTEGER,
    last_send TEXT
);

